import UIKit.UIColor

extension UIColor {
    enum LTech {
        static let backgroundСolorBlack = UIColor(named: "backgroundСolorBlack") ?? UIColor()
        static let backgroundСolorWhite = UIColor(named: "backgroundСolorWhite") ?? UIColor()
        static let backgroundСolorGreen = UIColor(named: "backgroundСolorGreen") ?? UIColor()
    }
}
